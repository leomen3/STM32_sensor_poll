/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	uint8_t value[10]; //a temp array to hold results of ADC conversion to string
	uint8_t i = 0; //loop index
	uint8_t x = 0; //temp vcariable

  /* USER CODE END 1 */
  

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC_Init();
  MX_USART2_UART_Init();
  MX_USART1_UART_Init();
  MX_TIM2_Init();
  MX_USB_PCD_Init();
  /* USER CODE BEGIN 2 */

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
		
		//print every second keep alive
		if (cnt%1000 == 0) {
			HAL_UART_Transmit_DMA(&huart2, uartBuff_keep, sizeof(uartBuff_keep));
			HAL_UART_Transmit_DMA(&huart1 , uartBuff_keep, sizeof(uartBuff_keep));
		}
		
		//Sample sound sensor
		HAL_ADC_Start(&hadc);
		if (HAL_ADC_PollForConversion(&hadc,5) == HAL_OK)
			adcVal0 = HAL_ADC_GetValue(&hadc);

		if (adcVal0 > SOUND_THRESHOLD) {
			x = adcVal0;
			i = 0; 
			do { // convert to char
				value[i++] = (char)(x % 10) + '0'; //convert integer to character
				x /= 10;
			} while(x);
			HAL_UART_Transmit_DMA(&huart1, uartBuff_alarm, sizeof(uartBuff_alarm));
			HAL_UART_Transmit_DMA(&huart2, uartBuff_alarm, sizeof(uartBuff_alarm));
			HAL_UART_Transmit_DMA(&huart2, value, sizeof(value));
			// ToDo Send String
			
			//HAL_UART_Transmit(&huart2, uartBuff2, 13, 10);
			adcVal0 = 0;
	  } 
		
		
		//wait 1ms
		HAL_Delay(50);
		cnt += 1;
		
  }
}
